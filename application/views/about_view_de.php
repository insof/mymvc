            <h2 id="link-C">Herstellung</h2>

            <p style="text-indent: 20px; margin-top: 20px;">Die Firma &quot;Lesnye Dary Zakarpatja&quot; wurde 2005 gegründet und arbeitet erfolgreich auf den ukrainischen und europäischen Märkten, mit qualitativ hochwertigen Produkten. Unser Unternehmen nutzt eine moderne Technologie um Beeren und Pilze einzufrieren und eine langjährige Erfahrung in der Verarbeitung und Logistik von Tiefkühlkost.</br></p>
            <p style="text-indent: 20px;">Das Einfrieren der Produkte geschieht innerhalb von 5-10min bei einer Temperatur von -32...- 35 ° C in einer &quot;kochenden Schicht&quot;. Das heißt, dass der kalte steigenden Luftstrom die Beeren oder das geschnittene Obst und das Gemüse in Bewegung hält. Das nahezu sofortige Gefrieren (Schockfrosten) des Produkts ermöglicht die Bildung großer Eiskristalle in dem Produkt zu verhindern. Dabei wird die interzellulare Schale des Produkts nicht beschädigt, so dass die aufgetauten Beeren sehen fast wie frische aus.</br></p>
            <img style="padding-left: 20%;" src="/imag/scheme_de.png">
            <p style="text-indent: 20px;">Frische Waldfrüchte kommen zuerst in die Vorkammer zur Vorkühlung und werden am gleichen Tag noch verarbeitet. In der ersten Bearbeitungsstufe gelangen die Beeren durch ein System aus Sieben und Gittern, wo die Beeren, die der Standard-Größe nicht entsprechen, aussortiert werden. Danach werden die Beeren mit einem Luftstrom bearbeitet, infolgedessen die Waldreste wie Laub und Moos entfernt werden. Dann kommen die Beeren in einen Tunnelfroster bei einer Temperatur von -32 ° C und einer Luftgeschwindigkeit von 3,6 m/s.</br>
            <p style="text-indent: 20px;">Als nächstes kommt die gefrorenen Beeren in einer speziellen Schraube, wo die qualitative Entstielen. Dann werden die Beeren sortiert, dozamorazhivayutsya und diente auf dem Inspektionstisch , wo die obligatorische Sichtkontrolle. Inspektor im Fall einer großen Anzahl von Defekten hat die Fähigkeit, den Betrieb des gesamten Reinigungslinie zu stoppen. In der letzten Phase bereits geschält und sortiert Beeren werden in Papiersäcken von 25 kg oder 10 kg Kartons verpackt.</br>  
            </p>
