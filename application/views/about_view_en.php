            <h2 id="link-C">MANUFACTURE</h2>

            <p style="text-indent: 20px; margin-top: 20px;">“Lesnye dary Zakarpatia” company was founded in 2005 and work successfully in the Ukrainian and European markets, providing high-quality products. We are using modern technology of shock freezing of berries and mushrooms and have many years&#39; experience in processing and logistics of frozen products.</br></p>
            <p style="text-indent: 20px;">Freezing of products is performed within 5-10 minutes at -32….35 C in “fluidized-bed”, so berries or slices of vegetables or fruits are moved under the influence of strong cold ascending air flow. In such conditions we get almost immediate freezing of the products (shock freezing), which helps to prevent forming the big ice crystals in the product, intercellular membrane of the product is preserved intact, so a frozen berry is almost identical to fresh one.</br></p>
            <img style="padding-left: 20%;" src="/imag/scheme_en.png">
            <p style="text-indent: 20px;">Raw products are put into the pre-cooling room and processed in the same day. On the first stage berries are processing according to system of sieves and treillages, on this stage berries, which are not appropriate to size standard, are fallen away. Then berries are processed by air flow for removing light forest wastes – leaves, moss. Then berries are put into quick-freezing room at -32С and 3.6 m/sec air velocity.</br>
            <p style="text-indent: 20px;">After that frozen berries come into special spiral conveyor, where performed careful removal of fruit stalks. Then berries are calibrated, additionally frozen and supplied on the inspection table where pass obligate visual control. An inspector, in case of large amount of defects, can stop work of all cleaning line. On the last stage clean and calibrated berries are packing into paper bags (25 kg each one) or сorrugated boxes (10 kg each one).</br>
            <p style="text-indent: 20px;">In case of agreement in advance, it is possible to pack in certain convenient container for Customer. We are interested in reliable permanent customers.
            </p>
